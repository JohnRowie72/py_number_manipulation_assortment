# ask the user for two numbers
num_1 = int(input("Enter base number: "))
num_2 = int(input("Enter exponent number: "))

# compute the result of raising the first number to the power of the second number
# print the result
result = num_1 ** num_2
print(f"The result of raising {num_1} to the power of {num_2} is {result}")
